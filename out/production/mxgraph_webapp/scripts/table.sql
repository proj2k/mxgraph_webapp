CREATE TABLE MX_GRAPH(MX_GRAPH_SEQ bigint COMMENT '다이어그램식별순번' PRIMARY KEY ,
   MODEL_XML CLOB COMMENT '다이어그램 관리용 xml',
   IMAGE_XML CLOB COMMENT '이미지 출력용 xml',
   IMAGE_WIDTH SMALLINT COMMENT '이미지 가로길이',
   IMAGE_HEIGHT SMALLINT COMMENT '이미지 세로길이',
   IMAGE_BG_COLOR CHAR(7) COMMENT '이미지 배경색, RGB Hexadecimal Colors, ex. #FFFFFF',
   CREATE_USER_ID VARCHAR(50) COMMENT '등록자 id',
   CREATE_DATE TIMESTAMP COMMENT '등록일시',
   UPDATE_USER_ID VARCHAR(50) COMMENT '변경자 id',
   UPDATE_DATE TIMESTAMP COMMENT '변경일시'
);
COMMENT ON TABLE MX_GRAPH IS 'mxgraph 다이어그램';

